package org.uncertweb.viss.core.web.provider;

import java.io.IOException;
import java.io.InputStream;
import java.lang.annotation.Annotation;
import java.lang.reflect.Type;

import javax.ws.rs.WebApplicationException;
import javax.ws.rs.core.MediaType;
import javax.ws.rs.core.MultivaluedMap;
import javax.ws.rs.ext.MessageBodyReader;
import javax.ws.rs.ext.Provider;

import org.apache.commons.io.IOUtils;
import org.codehaus.jettison.json.JSONException;
import org.codehaus.jettison.json.JSONObject;
import org.slf4j.Logger;
import org.slf4j.LoggerFactory;
import org.uncertweb.viss.core.VissError;

/**
 * TODO JavaDoc
 *
 * @author Christian Autermann
 */
@Provider
public class GenericJSONMessageBodyReader implements MessageBodyReader<JSONObject> {

    private static final Logger log = LoggerFactory.getLogger(GenericJSONMessageBodyReader.class);

    @Override
    public boolean isReadable(Class<?> type, Type genericType, Annotation[] annotations, MediaType mediaType) {
        return type.isAssignableFrom(JSONObject.class) &&
               mediaType.getType().equals("application") &&
               mediaType.getSubtype().endsWith("json");
    }

    @Override
    public JSONObject readFrom(Class<JSONObject> type, Type genericType, Annotation[] annotations, MediaType mediaType,
                               MultivaluedMap<String, String> httpHeaders, InputStream entityStream) throws IOException,
                                                                                                            WebApplicationException {
        try {
            return new JSONObject(IOUtils.toString(entityStream));
        } catch (JSONException ex) {
            throw VissError.badRequest(ex);
        }
    }

}
